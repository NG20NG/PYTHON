class FunctionsCalculator:
    def addition(self, x, y):
        res = int(x + y)
        return res

    def subtraction(self, x, y):
        res = int(x - y)
        return res

    def darb(self, x, y):
        res = int(x * y)
        return res

    def kisma(self, x, y):
        res = int(x / y)
        return res
